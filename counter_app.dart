import 'package:flutter/material.dart';

void main() {
runApp(const CounterApp());
}

class CounterApp extends StatelessWidget { const CounterApp({super.key});

@override
Widget build(BuildContext context) { return const MaterialApp( debugShowCheckedModeBanner: false, home: CounterScreen(),
);
}
}

class CounterScreen extends StatefulWidget { const CounterScreen({super.key});

@override
_CounterScreenState createState() => _CounterScreenState();
}

class _CounterScreenState extends State<CounterScreen> { int _counter = 0;

void _incrementCounter() { setState(() {
_counter++;
});
}

@override
Widget build(BuildContext context) { return Scaffold(
appBar: AppBar(title: const Text('Counter App')), body: Center(
child: Text(
'Counter: $_counter',
style: const TextStyle(fontSize: 24),
),
),
floatingActionButton: FloatingActionButton( onPressed: _incrementCounter,
child: const Icon(Icons.add), // Perbaikan disini
),
);
}
}
